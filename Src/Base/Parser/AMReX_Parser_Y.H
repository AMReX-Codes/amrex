enum parser_node_t {
    PARSER_NUMBER = 1,
    PARSER_SYMBOL,
    PARSER_ADD,
    PARSER_SUB,
    PARSER_MUL,
    PARSER_DIV,
    PARSER_NEG,
    PARSER_F1,
    PARSER_F2,
    PARSER_F3,
    PARSER_ASSIGN,
    PARSER_LIST,
    PARSER_ADD_VP,  /* types below are generated by optimization */
    PARSER_ADD_PP,
    PARSER_SUB_VP,
    PARSER_SUB_PP,
    PARSER_MUL_VP,
    PARSER_MUL_PP,
    PARSER_DIV_VP,
    PARSER_DIV_PP,
    PARSER_NEG_P
};

/* In C, the address of the first member of a struct is the same as
 * the address of the struct itself.  Because of this, all struct parser_*
 * pointers can be passed around as struct parser_node pointer and enum
 * parser_node_t type can be safely checked to determine their real type.
 */

union parser_nvp {
    struct parser_node* n;
    double v;
    int ip;
};

struct parser_node {
    enum parser_node_t type;
    struct parser_node* l;
    struct parser_node* r;
    union parser_nvp lvp; // After optimization, this may store left value/pointer offset.
    int rip;              //                     this may store right      pointer offset.
};

struct parser_number {
    enum parser_node_t type;
    double value;
};

struct parser_symbol {
    enum parser_node_t type;
    char* name;
    int ip;
};

struct parser_f1 {  /* Builtin functions with one argument */
    enum parser_node_t type;
    struct parser_node* l;
    enum parser_f1_t ftype;
};

struct parser_f2 {  /* Builtin functions with two arguments */
    enum parser_node_t type;
    struct parser_node* l;
    struct parser_node* r;
    enum parser_f2_t ftype;
};

struct parser_f3 { /* Builtin functions with three arguments */
    enum parser_node_t type;
    struct parser_node* n1;
    struct parser_node* n2;
    struct parser_node* n3;
    enum parser_f3_t ftype;
};

struct parser_assign {
    enum parser_node_t type;
    struct parser_symbol* s;
    struct parser_node* v;
};

static_assert(sizeof(parser_f3) <= sizeof(parser_node), "amrex parser: sizeof parser_node too small");

